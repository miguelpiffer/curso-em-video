a1=input ('digite algo')
print('o tipo primitivo desse valor é',type(a1))
print('é um numero?', a1.isnumeric())
print('é alfabético?',a1.isalpha())
print('é um espaço ?',a1.isspace())
print('é alfanumérico?',a1.isalnum())
print('está em maiúsculas?',a1.isupper())
print('está em minusculas?',a1.islower())
print('etsá captalizada?',a1.istitle())






